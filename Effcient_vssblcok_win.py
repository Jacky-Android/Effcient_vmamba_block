        deltaA = torch.exp(einsum(delta, A,'b l d, d n -> b l d n'))   # (B,L,D) * (D,N) -> (B,L,D,N)
        # 将 delta、B 和 u 张量的对应位置元素相乘，并在最后一个维度上进行求和，输出一个新的张量。
